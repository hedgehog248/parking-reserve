window.addEventListener('load', function(){
  const buildNum = document.getElementById("user_building_num");
  buildNum.addEventListener('change', function(){
    roomNum = document.getElementById("user_room_num");
    roomNum.options.length = 0;
    const changedBuildVal = this.value;
    for (let i = 0; i < arr[changedBuildVal].length; i++) {
      const newOpt = document.createElement("option");
      const value = arr[changedBuildVal][i];
      newOpt.value = value.cd;
      newOpt.text = value.label;
      roomNum.appendChild(newOpt);
    }
  });
})